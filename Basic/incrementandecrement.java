package Basic;

public class incrementandecrement {
    public static void main(String[] args) {
        int a = 5 ;
        a --;
        System.out.println(a);
        int b = 5 ;
        b ++;
        System.out.println(b);

        System.out.println(--a);
        System.out.println(++b);
    
    }
}
