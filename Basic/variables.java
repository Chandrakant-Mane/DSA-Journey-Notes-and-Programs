package Basic;

public class variables {
    public static void main(String[] args) {
        int x; // declaration
        x = 6; // initialization
        System.out.println("x : " + x);
        // x = 10;
        // System.out.println("x : " + x);

        int y = 5; // declaration and initialization
        System.out.println("y : " + y);
        y = 7; // y = 7
        System.out.println("y : " + y);
        y += 6; // y = 7 + 6 = 13
        System.out.println("y : " + y);
        y -= 20; // y = 13 - 20 = -7
        System.out.println("y : " + y);
        y *= 7; // y = -7 * 7 = - 49
        System.out.println("y : " + y);
        y /= 7; // y = -49 / 7 = -7
        System.out.println("y : " + y);

    }

}
 