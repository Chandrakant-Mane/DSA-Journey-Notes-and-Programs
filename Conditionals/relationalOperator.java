package Conditionals;

public class relationalOperator {
    public static void main(String[] args) {
        int a = 8 , b = 6 ;
        System.out.println(a!=b);

        System.out.println(a<b);
        System.out.println(a>b);

        System.out.println(a<=b);
        System.out.println(a>=b);


        // EqualityOperator

        System.out.println(a==b); // equal to 
        System.out.println(a!=b); // not equal to 

    }
    
}
